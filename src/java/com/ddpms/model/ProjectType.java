package com.ddpms.model;

public class ProjectType {

    private String protId;
    private String protCode;
    private String protName;
    private String protType;
    private String modifiedDate;
    private String modifiedBy;

    public String getProtId() {
        return protId;
    }

    public void setProtId(String protId) {
        this.protId = protId;
    }

    public String getProtCode() {
        return protCode;
    }

    public void setProtCode(String protCode) {
        this.protCode = protCode;
    }

    public String getProtName() {
        return protName;
    }

    public void setProtName(String protName) {
        this.protName = protName;
    }

    public String getProtType() {
        return protType;
    }

    public void setProtType(String protType) {
        this.protType = protType;
    }

    public String getModifiedDate() {
        return modifiedDate;
    }

    public void setModifiedDate(String modifiedDate) {
        this.modifiedDate = modifiedDate;
    }

    public String getModifiedBy() {
        return modifiedBy;
    }

    public void setModifiedBy(String modifiedBy) {
        this.modifiedBy = modifiedBy;
    }
    
}

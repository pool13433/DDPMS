
package com.ddpms.model;

public class ProjectHistory {
    private String hisId;
    private String projId;
    private String status;
    private String remarks;
    private String modifiedDate;
    private String modifiedBy;
    private String sumProjectStatus;

    public String getHisId() {
        return hisId;
    }

    public void setHisId(String hisId) {
        this.hisId = hisId;
    }

    public String getProjId() {
        return projId;
    }

    public void setProjId(String projId) {
        this.projId = projId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public String getModifiedDate() {
        return modifiedDate;
    }

    public void setModifiedDate(String modifiedDate) {
        this.modifiedDate = modifiedDate;
    }

    public String getModifiedBy() {
        return modifiedBy;
    }

    public void setModifiedBy(String modifiedBy) {
        this.modifiedBy = modifiedBy;
    }

    public String getSumProjectStatus() {
        return sumProjectStatus;
    }

    public void setSumProjectStatus(String sumProjectStatus) {
        this.sumProjectStatus = sumProjectStatus;
    }
    
    
    
}
